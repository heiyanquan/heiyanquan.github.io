<h4 id="breaking-changes-6"><a class="header-anchor" href="#breaking-changes-6">¶</a> Breaking changes</h4>
<ul>
<li>General
<ul>
<li>Eliminado <code>theme-default</code></li>
<li>Compatible con Vue 2.5.2+ e IE 10+</li>
<li>Evento <code>change</code> de componentes del formulario y evento <code>current-change</code> de Pagination ahora sólo se activa en la interacción del usuario.</li>
<li>El atributo <code>size</code> del botón y los componentes del formulario aceptan ahora los tamaños <code>medium</code>, <code>small</code> y <code>mini</code>.</li>
<li>Para facilitar el uso de iconos de terceros, los atributos <code>icon</code> de Button y Steps y los atributos <code>prefix-icon</code> y <code>suffix-icon</code> del input ahora requieren un nombre de clase completo.</li>
</ul>
</li>
<li>Dialog
<ul>
<li>Atributo <code>size</code> eliminado. Ahora el tamaño de Dialog se puede configurar con <code>width</code> y <code>fullscreen</code></li>
<li>Ahora la visibilidad del Diálogo no puede ser controlada por <code>v-model</code></li>
</ul>
</li>
<li>Rate
<ul>
<li><code>text-template</code> a sido renombrado a <code>score-template</code></li>
</ul>
</li>
<li>Dropdown
<ul>
<li><code>menu-align</code> a sido renombrado a <code>placement</code>. Ahora soporta más posiciones</li>
</ul>
</li>
<li>Transfer
<ul>
<li><code>footer-format</code> a sido renombrado a <code>format</code></li>
</ul>
</li>
<li>Switch
<ul>
<li>Los atributos que comienzan con <code>on*</code> serán analizados en eventos en JSX, haciendo que todos los atributos <code>on*</code> de Switch no puedan trabajar en JSX. Por lo tanto, los atributos <code>on*</code> se renombran a <code>active-*</code>, y por consiguiente los atributos <code>off-*</code> se renombran a <code>inactivado-*</code>. Este cambio afecta a los siguientes atributos: <code>on-icon-class</code>, <code>off-icon-class</code>, <code>on-text</code>, <code>off-text</code>, <code>on-color</code>, <code>off-color</code>, <code>on-value</code>, <code>off-value</code>.</li>
<li>Los atributos <code>active-text</code> y <code>inactive-text</code> ahora no tienen valores por defecto.</li>
</ul>
</li>
<li>Tag
<ul>
<li>El atributo type acepta ahora <code>success</code>, <code>info</code>, <code>warning</code> y <code>danger</code></li>
</ul>
</li>
<li>Menu
<ul>
<li>Atributo <code>theme</code> eliminado. El color de Menu se puede configurar utilizando <code>background-color</code>, <code>text-color</code> y <code>active-text-color</code></li>
