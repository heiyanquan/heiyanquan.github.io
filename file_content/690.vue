<h2 id="cascader-ji-lian-xuan-ze-qi"><a class="header-anchor" href="#cascader-ji-lian-xuan-ze-qi">¶</a> Cascader 级联选择器</h2>
<p>当一个数据集合有清晰的层级结构时，可通过级联选择器逐级查看并选择。</p>
<h3 id="ji-chu-yong-fa"><a class="header-anchor" href="#ji-chu-yong-fa">¶</a> 基础用法</h3>
<p>有两种触发子菜单的方式</p>
<demo-block>
        <div><p>只需为 Cascader 的<code>options</code>属性指定选项数组即可渲染出一个级联选择器。通过<code>props.expandTrigger</code>可以定义展开子级菜单的触发方式。</p>
</div>
