<h2 id="instalacion"><a class="header-anchor" href="#instalacion">¶</a> Instalación</h2>
<h3 id="npm"><a class="header-anchor" href="#npm">¶</a> npm</h3>
<p>Instalar mediante npm es la forma recomendada ya que se integra fácilmente con <a href="https://webpack.js.org/">webpack</a>.</p>
<pre><code class="language-shell">npm i element-ui -S
</code></pre>
<h3 id="cdn"><a class="header-anchor" href="#cdn">¶</a> CDN</h3>
<p>Obtenga la última versión desde <a href="https://unpkg.com/element-ui/">unpkg.com/element-ui</a> , e importe el JavaScript y los archivos CSS en su página.</p>
<pre><code class="language-html">&lt;!-- import CSS --&gt;
&lt;link rel=&quot;stylesheet&quot; href=&quot;https://unpkg.com/element-ui/lib/theme-chalk/index.css&quot;&gt;
&lt;!-- import JavaScript --&gt;
&lt;script src=&quot;https://unpkg.com/element-ui/lib/index.js&quot;&gt;&lt;/script&gt;
</code></pre>
<p>##Tip
Recomendamos a nuestros usuarios congelar la versión de Element cuando usas un CDN. Por favor, refiérase a <a href="https://unpkg.com">unpkg.com</a> para más información.</p>
