<h2 id="installation"><a class="header-anchor" href="#installation">¶</a> Installation</h2>
<h3 id="npm"><a class="header-anchor" href="#npm">¶</a> npm</h3>
<p>Installing with npm is recommended and it works seamlessly with <a href="https://webpack.js.org/">webpack</a>.</p>
<pre><code class="language-shell">npm i element-ui -S
</code></pre>
<h3 id="cdn"><a class="header-anchor" href="#cdn">¶</a> CDN</h3>
<p>Get the latest version from <a href="https://unpkg.com/element-ui/">unpkg.com/element-ui</a> , and import JavaScript and CSS file in your page.</p>
<pre><code class="language-html">&lt;!-- import CSS --&gt;
&lt;link rel=&quot;stylesheet&quot; href=&quot;https://unpkg.com/element-ui/lib/theme-chalk/index.css&quot;&gt;
&lt;!-- import JavaScript --&gt;
&lt;script src=&quot;https://unpkg.com/element-ui/lib/index.js&quot;&gt;&lt;/script&gt;
</code></pre>
<div class="tip">
<p>We recommend our users to lock Element's version when using CDN. Please refer to <a href="https://unpkg.com">unpkg.com</a> for more information.</p>
</div>
