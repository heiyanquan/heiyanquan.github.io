<h2 id="installation"><a class="header-anchor" href="#installation">¶</a> Installation</h2>
<h3 id="npm"><a class="header-anchor" href="#npm">¶</a> NPM</h3>
<p>Installer Element via npm est recommandé, il fonctionne parfaitement avec <a href="https://webpack.js.org/">webpack</a>.</p>
<pre><code class="language-shell">npm i element-ui -S
</code></pre>
<h3 id="cdn"><a class="header-anchor" href="#cdn">¶</a> CDN</h3>
<p>Obtenez la dernière version via <a href="https://unpkg.com/element-ui/">unpkg.com/element-ui</a>, et importez le JavaScript et le CSS dans votre page.</p>
<pre><code class="language-html">&lt;!-- import du CSS --&gt;
&lt;link rel=&quot;stylesheet&quot; href=&quot;https://unpkg.com/element-ui/lib/theme-chalk/index.css&quot;&gt;
&lt;!-- import du JavaScript --&gt;
&lt;script src=&quot;https://unpkg.com/element-ui/lib/index.js&quot;&gt;&lt;/script&gt;
</code></pre>
<div class="tip">
<p>Il est recommandé de fixer la version d'Element lors de l'utilisation du CDN. Référez-vous à  <a href="https://unpkg.com">unpkg.com</a> pour plus d'informations.</p>
</div>
