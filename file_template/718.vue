<h4 id="breaking-changes-5"><a class="header-anchor" href="#breaking-changes-5">¶</a> Breaking changes</h4>
<ul>
<li>Général
<ul>
<li>Suppression de <code>theme-default</code>.</li>
<li>Compatible avec Vue 2.5.2+ et IE 10+</li>
<li>L'événement <code>change</code> des composants de formulaire et l'événement <code>current-current-change</code> de la pagination ne se déclenchent plus que lors de l'interaction de l'utilisateur</li>
<li>L'attribut <code>size</code> de Button et les composants de formulaire acceptent maintenant <code>medium</code>, <code>small</code> et <code>mini</code></li>
<li>Pour faciliter l'utilisation d'icônes tierces, les attributs <code>icon</code> du bouton et des étapes, <code>prefix-icon</code> et <code>suffix-icon</code> d'Input nécessitent maintenant un nom de classe complet</li>
</ul>
</li>
<li>Dialog
<ul>
<li>Suppression de l'attribut <code>taille</code>. Maintenant la taille de Dialog peut être configurée par <code>width</code> et <code>fullscreen</code></li>
<li>Maintenant la visibilité de Dialog ne peut plus être contrôlée par <code>v-model</code></li>
</ul>
</li>
<li>Rate
<ul>
<li>Le <code>text-template</code> est renommé <code>score-template</code></li>
</ul>
</li>
<li>Dropdown
<ul>
<li><code>menu-align</code> est renommé en <code>placement</code>. Maintenant il supporte plus de positions</li>
</ul>
</li>
<li>Transfert
<ul>
<li>le <code>footer-format</code> est renommé en <code>format</code></li>
</ul>
</li>
<li>Switch
<ul>
<li>Les attributs commençant par <code>on-**</code> seront analysés pour les événements dans JSX, ce qui rend tous les attributs <code>on-*</code> de Switch incapable de fonctionner en JSX. Ainsi, les attributs <code>on-**</code> sont renommés en <code>active-*</code>, et par conséquent les attributs <code>off-*</code> sont renommés en <code>inactive-*</code>. Cette modification affecte les attributs suivants: <code>on-icon-class</code>, <code>off-icon-class</code>, <code>on-text</code>, <code>off-text</code>, <code>off-text</code>, <code>on-color</code>, <code>off-color</code>, <code>on-value</code>, <code>off-value</code></li>
<li>Les attributs <code>active-text</code> et <code>inactive-text</code> n'ont plus de valeurs par défaut</li>
</ul>
</li>
<li>Tag
<ul>
<li>L'attribut <code>type</code> accepte maintenant <code>success</code>, <code>info</code>, <code>warning</code> et <code>danger</code></li>
</ul>
</li>
<li>Menu
<ul>
<li>Suppression de l'attribut <code>theme</code>. La couleur du menu peut être configurée en utilisant <code>background-color</code>, <code>text-color</code> et <code>active-text-color</code></li>
</ul>
</li>
<li>Input
<ul>
<li>Suppression de l'attribut <code>icon</code>. L'icône du suffixe peut maintenant être configurée à l'aide de l'attribut <code>suffix-icon</code> ou du slot <code>suffix-icon</code></li>
<li>Suppression de l'attribut <code>on-icon-click</code> et de l'événement <code>click</code>. Maintenant pour ajouter le gestionnaire de clic sur les icônes, veuillez utiliser les slots nommés</li>
<li>L'événement <code>change</code> se comporte maintenant comme dans l'input natif, qui ne se déclenche qu'en cas de blur ou en appuyant sur Entrée. Si vous avez besoin de répondre à l'entrée de l'utilisateur en temps réel, vous pouvez utiliser l'événement <code>input</code></li>
