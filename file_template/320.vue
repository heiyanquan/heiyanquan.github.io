
<h2 id="color"><a class="header-anchor" href="#color">¶</a> Color</h2>
<p>Element utiliza un conjunto de paletas para especificar colores, y así, proporcionar una apariencia y sensación coherente para los productos que construye.</p>
<h3 id="color-principal"><a class="header-anchor" href="#color-principal">¶</a> Color principal</h3>
<p>El color principal de Element es el azul brillante y amigable.</p>
